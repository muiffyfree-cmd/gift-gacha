export function getPriceRecipientTitle(gender: string, recipient: string): string | undefined {
  if (!gender || !recipient) return undefined;
  return HAND_WRITTEN_TITLES[recipient]?.[gender];
}

export function getPriceRecipientDescription(gender: string, recipient: string): string | undefined {
  if (!gender || !recipient) return undefined;

  const handWritten = HAND_WRITTEN_DESCRIPTIONS[recipient]?.[gender];
  if (handWritten) return handWritten;

  if (gender === GENDER_UNRESTRICTED_TAG) {
    return `${recipient}へ贈る誕生日プレゼントのおすすめを紹介。種類や予算から絞り込んで選べます。`;
  }
  return `${gender}の${recipient}へ贈る誕生日プレゼントのおすすめを紹介。種類や予算から絞り込んで選べます。`;
}
