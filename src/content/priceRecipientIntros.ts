export function getPriceRecipientIntro(gender: string, recipient: string): string | undefined {
  if (!gender || !recipient) return undefined;

  const handWritten = HAND_WRITTEN_INTROS[recipient]?.[gender];
  if (handWritten) return handWritten;

  if (gender === GENDER_UNRESTRICTED_TAG) {
    const recipientHint =
      RECIPIENT_PHRASES[recipient] ?? `${recipient}に喜ばれる誕生日プレゼントを集めました。`;
    return `${recipient}への誕生日プレゼント特集です。${recipientHint}種類や予算からも絞り込めるので、ぴったりの一品を見つけてみてください。`;
  }

  const genderHint = GENDER_PHRASES[gender] ?? "";
  const recipientHint =
    RECIPIENT_PHRASES[recipient] ?? `${recipient}に喜ばれる誕生日プレゼントを集めました。`;
  return `${gender}へ贈る、${recipient}への誕生日プレゼント特集です。${recipientHint}${genderHint}種類や予算からも絞り込めるので、ぴったりの一品を見つけてみてください。`;
}
